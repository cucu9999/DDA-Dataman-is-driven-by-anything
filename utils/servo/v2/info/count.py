pwm_1_r = {}
for key,data in pwm.items():
    id = key
    start = data[0]
    max_data = data[1]
    min_data = data[2]
    data_r = []
    data_r.append(90)
    max_r = (180/2000) * (max_data-500)
    data_r.append(int(max_r))
    min_r = (180/2000) * (min_data-500)
    data_r.append(int(min_r))
    start_pro = abs(min(max_r,min_r) - 90)/abs(max_r-min_r)
    data_r.append(round(start_pro, 2))
    pwm_1_r[id] = data_r
print(pwm_1_r)
